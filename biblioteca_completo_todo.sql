
-- CREACIÓN DE TABLAS CON RESTRICCIONES Y CLAVES FORÁNEAS

CREATE TABLE Categoria (
    ID_Categoria SERIAL PRIMARY KEY,
    Nombre_Categoria VARCHAR(100) NOT NULL UNIQUE,
    Descripcion TEXT DEFAULT 'Sin descripción'
);

CREATE TABLE Editorial (
    ID_Editorial SERIAL PRIMARY KEY,
    Nombre VARCHAR(100) NOT NULL UNIQUE,
    Pais VARCHAR(50) NOT NULL,
    Anio_Fundacion INT CHECK (Anio_Fundacion > 1500)
);

CREATE TABLE Usuario (
    ID_Usuario SERIAL PRIMARY KEY,
    Nombre VARCHAR(100) NOT NULL,
    Apellido VARCHAR(100) NOT NULL,
    Email VARCHAR(150) UNIQUE NOT NULL,
    Telefono VARCHAR(20) UNIQUE NOT NULL,
    Direccion TEXT NOT NULL,
    Tipo_Usuario VARCHAR(20) NOT NULL CHECK (Tipo_Usuario IN ('Alumno', 'Profesor', 'Externo'))
);

CREATE TABLE Libro (
    ID_Libro SERIAL PRIMARY KEY,
    Titulo VARCHAR(255) NOT NULL,
    ISBN VARCHAR(20) UNIQUE NOT NULL,
    Anio_Publicacion INT CHECK (Anio_Publicacion > 1400),
    Idioma VARCHAR(50) NOT NULL,
    Resumen TEXT,
    Autores TEXT[] NOT NULL,
    ID_Editorial INT NOT NULL REFERENCES Editorial(ID_Editorial),
    ID_Categoria INT NOT NULL REFERENCES Categoria(ID_Categoria)
);

CREATE TABLE UnidadLibro (
    ID_Unidad SERIAL PRIMARY KEY,
    Codigo_Barra VARCHAR(30) UNIQUE NOT NULL,
    ID_Libro INT NOT NULL REFERENCES Libro(ID_Libro),
    Ubicacion_Fisica VARCHAR(100) NOT NULL,
    Estado_Fisico VARCHAR(50) NOT NULL CHECK (Estado_Fisico IN ('Nuevo', 'Bueno', 'Regular', 'Malo'))
);

CREATE TABLE Prestamo (
    ID_Prestamo SERIAL PRIMARY KEY,
    Fecha_Prestamo DATE NOT NULL DEFAULT CURRENT_DATE,
    Fecha_Entrega DATE,
    ID_Usuario INT NOT NULL REFERENCES Usuario(ID_Usuario),
    Estado_Prestamo VARCHAR(20) NOT NULL CHECK (Estado_Prestamo IN ('Pendiente', 'Devuelto', 'Retrasado'))
);

CREATE TABLE Prestamo_UnidadLibro (
    ID_Prestamo INT NOT NULL REFERENCES Prestamo(ID_Prestamo),
    ID_Unidad INT NOT NULL REFERENCES UnidadLibro(ID_Unidad),
    PRIMARY KEY (ID_Prestamo, ID_Unidad)
);

CREATE TABLE Reserva (
    ID_Reserva SERIAL PRIMARY KEY,
    Fecha_Reserva DATE DEFAULT CURRENT_DATE,
    Estado_Reserva VARCHAR(20) NOT NULL CHECK (Estado_Reserva IN ('Activa', 'Cancelada')),
    ID_Usuario INT NOT NULL REFERENCES Usuario(ID_Usuario),
    ID_Unidad INT NOT NULL REFERENCES UnidadLibro(ID_Unidad)
);

CREATE TABLE Usuario_Categoria_Interes (
    ID_Usuario INT NOT NULL REFERENCES Usuario(ID_Usuario),
    ID_Categoria INT NOT NULL REFERENCES Categoria(ID_Categoria),
    PRIMARY KEY (ID_Usuario, ID_Categoria)
);

CREATE TABLE Historial_Acceso (
    ID_Acceso SERIAL PRIMARY KEY,
    ID_Usuario INT NOT NULL REFERENCES Usuario(ID_Usuario),
    ID_Libro INT NOT NULL REFERENCES Libro(ID_Libro),
    Fecha_Consulta DATE DEFAULT CURRENT_DATE,
    Tipo_Acceso VARCHAR(20) CHECK (Tipo_Acceso IN ('Lectura', 'Descarga', 'Visualización'))
);


